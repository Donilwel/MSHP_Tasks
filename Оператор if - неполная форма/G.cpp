#include <iostream>
using namespace std;
int main() {
    int a, b, c;
    cin >> a >> b >> c;
    if (a >= 0){
        a > 0;
    }
    if (b >= 0) {
        b > 0;
    }
    if (c >= 0) {
        c > 0;
    }
    if (a < 0) {
        a = 0;
    }
    if (b < 0) {
        b = 0;
    }
    if (c < 0) {
        c = 0;
    }
    cout << (a + b + c);
    return 0;
}
